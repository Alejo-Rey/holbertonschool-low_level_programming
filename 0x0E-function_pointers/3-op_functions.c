#include "3-calc.h"

/**
 * op_add - function to add
 * @a: variable one
 * @b: variable two
 * Return: int
 */
int op_add(int a, int b)
{	
	return (a + b);
}
/**
 * op_sub - function to sub
 * @a: variable one
 * @b: variable two
 * Return: int
 */
int op_sub(int a, int b)
{	
	return (a - b);
}

/**
 * op_mul - function to mul
 * @a: variable one
 * @b: variable two
 * Return: int
 */
int op_mul(int a, int b)
{	
	return (a * b);
}

/**
 * op_div - function to div
 * @a: variable one
 * @b: variable two
 * Return: int
 */
int op_div(int a, int b)
{	
	return (a / b);
}

/**
 * op_mod - function to mod
 * @a: variable one
 * @b: variable two
 * Return: int
 */
int op_mod(int a, int b)
{	
	return (a % b);
}
